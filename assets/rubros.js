class Rubros {
	constructor(data, i){
    this._url = data.url;
    this._icono = data.icono;
    this._nombre = data.nombre;
    this._base = `
      	<div class="col-md-2 col-sm-4">
	        <div class="media rubro">
                <a href="` + this._url + `">
                  <div>
                    <div class="title rubros"><img class="iconoTrans" src="` + this._icono + `"></div>
                    <div class="nombreRubro">
                      <span class="small">` + this._nombre + `</span>
                    </div>
                  </div>
                </a>
	        </div>

        </div>
    `;
  }

  getHTML(){
    return this._base;
  }

}

function renderRubros(){
  rubros.forEach(function(_app_, i){
    const newRubros = new Rubros(_app_, i);
     $(".rubrosDesk").append(newRubros.getHTML());
  });
}

